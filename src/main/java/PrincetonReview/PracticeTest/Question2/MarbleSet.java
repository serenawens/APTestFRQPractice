package PrincetonReview.PracticeTest.Question2;

public class MarbleSet {
    private String color;
    private int number;

    public MarbleSet(String marbleColor, int numMarbles) {
        color = marbleColor;
        number = numMarbles;
    }

    public int getNumber() {
        return number;
    }

    public String getColor() {
        return color;
    }
}
