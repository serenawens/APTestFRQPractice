package FiveSteps.PracticeExam1.Question1;

public class CoordinatePoint {
    private double x;
    private double y;

    public CoordinatePoint(double X, double Y)
    {
        x = X;
        y = Y;
    }

    public double getX() {
        return x;
    }

    public double getY() {
        return y;
    }
}
